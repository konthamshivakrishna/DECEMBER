package practise;

public class IfelseStatements3_1 {

	public static void main(String args[])
	{
		int n=4;
		
		if(n==0) 
		{	
			System.out.println("Nothing");
			System.out.println("Bye");
		}	
		else if(n%2==0)
		{
			System.out.println("Even");
		}
		else
		{
			System.out.println("Odd");
		}
	}
	}